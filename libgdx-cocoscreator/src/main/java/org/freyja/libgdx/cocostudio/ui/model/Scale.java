package org.freyja.libgdx.cocostudio.ui.model;

public class Scale {
	float ScaleX;
	float ScaleY;

	public float getScaleX() {
		return ScaleX;
	}

	public void setScaleX(float scaleX) {
		ScaleX = scaleX;
	}

	public float getScaleY() {
		return ScaleY;
	}

	public void setScaleY(float scaleY) {
		ScaleY = scaleY;
	}

}
