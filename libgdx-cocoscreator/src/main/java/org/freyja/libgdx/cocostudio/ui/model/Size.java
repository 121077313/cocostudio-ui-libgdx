package org.freyja.libgdx.cocostudio.ui.model;

import org.freyja.libgdx.cocos.creator.ui.model.BaseNode;

public class Size extends BaseNode{

	float width;

	float height;

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}


}
