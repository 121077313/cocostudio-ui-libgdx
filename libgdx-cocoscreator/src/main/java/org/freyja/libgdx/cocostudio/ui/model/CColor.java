package org.freyja.libgdx.cocostudio.ui.model;

import org.freyja.libgdx.cocos.creator.ui.model.BaseNode;

public class CColor extends BaseNode{
	int r;

	int g;

	int b;

	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

	public int getG() {
		return g;
	}

	public void setG(int g) {
		this.g = g;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

}
